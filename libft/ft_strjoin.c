#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	size;
	char	*str;
	int		i;

	size = ft_strlen(s1) + ft_strlen(s2) + 1;
	i = 0;
	str = malloc(size * sizeof(char));
	if (!str)
		return (str);
	while (*s1)
	{
		str[i] = *s1;
		s1++;
		i++;
	}
	while (*s2)
	{
		str[i] = *s2;
		s2++;
		i++;
	}
	str[i] = '\0';
	return (str);
}
